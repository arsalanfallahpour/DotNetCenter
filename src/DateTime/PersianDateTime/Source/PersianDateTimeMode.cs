namespace DotNetCenter.DateTime.Persian
{
    /// <summary>
    /// Specifies Format of a Date-Time
    /// </summary>
    public enum PersianDateTimeMode
    {
        /// <summary>
        /// Format of the Date-Time format is System
        /// </summary>
        System,
        /// <summary>
        /// Format of the Date-Time is Time-Zone
        /// </summary>
        PersianTimeZoneInfo,
        /// <summary>
        /// Format of the Date-Time is Utc Offset 
        /// </summary>
        UtcOffset
    }
}
